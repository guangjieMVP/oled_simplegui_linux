/** Function Name:  StepNext_GB2312                                     **/
/** Purpose:        Read current character code order by input pointer  **/
/**                 and step to next character start pointer.           **/
/** Resources:      None.                                               **/
/** Params:                                                             **/
/** @ cszSrc[in]:   Current char pointer.                               **/
/** @ puiCode[in]:  Character code.                                     **/
/** Return:         Next character start pointer.                       **/
/*************************************************************************/
static SGUI_CSZSTR StepNext_Chinese(SGUI_CSZSTR cszSrc, SGUI_UINT32* puiCode)
{
    /*----------------------------------*/
    /* Variable Declaration             */
    /*----------------------------------*/
    const SGUI_CHAR*            pcNextChar;
    SGUI_UINT32                 uiCode;

    /*----------------------------------*/
    /* Initialize                       */
    /*----------------------------------*/
    pcNextChar =                cszSrc;
    uiCode =                    0;

    /*----------------------------------*/
    /* Process                          */
    /*----------------------------------*/
    if(NULL != pcNextChar)
    {
        do
        {
            uiCode = (SGUI_BYTE)(*pcNextChar++);
            if(uiCode < 0x7F)
            {
                break;
            }
            uiCode = uiCode<<8;
            uiCode |= (SGUI_BYTE)(*pcNextChar++);
        }while(0);
    }
    *puiCode = uiCode;
    g_charCodeVal = uiCode;
    MYFONT_LOG("uicode %x\r\n", *puiCode);
    return pcNextChar;
}

/*************************************************************************/
/** Function Name:  GB2312_GetFontData                                  **/
/** Purpose:        Read character data form font data.                 **/
/** Resources:      None.                                               **/
/** Params:                                                             **/
/** @ sStartAddr[in]: Read start address in memory.                     **/
/** @ pDataBuffer[in]: Character data dump buffer pointer.              **/
/** @ sReadSize[in]: Size of data will be read, always mean the buffer  **/
/**                 size.                                               **/
/** Return:         Data in process was be read.                        **/
/*************************************************************************/
static SGUI_SIZE GetFontData_Chinese(SGUI_SIZE sStartAddr, SGUI_BYTE* pDataBuffer, SGUI_SIZE sReadSize)
{
    /*----------------------------------*/
    /* Variable Declaration             */
    /*----------------------------------*/
    SGUI_SIZE                   sReadCount;
    const SGUI_BYTE*            pSrc = GB2312_Font_Data+sStartAddr;
    SGUI_BYTE*                  pDest = pDataBuffer;

    /*----------------------------------*/
    /* Initialize                       */
    /*----------------------------------*/

    pDest = pDataBuffer;

    if (IsFullWidth_Chinse(g_charCodeVal))
    {
        //   sReadSize = 24;
        MYFONT_LOG("full width char %d\r\n", sReadSize);
        pSrc = GB2312_Font_Data+MYFONT_ASCII_CHAR_END*12 + 24*(g_charCodeIndex-MYFONT_ASCII_CHAR_END);
      
    }
    else
    {
        //   sReadSize = 12;
        MYFONT_LOG("half width char %d\r\n", sReadSize);
        pSrc = GB2312_Font_Data+12*g_charCodeIndex;
    }

    /*----------------------------------*/
    /* Process                          */
    /*----------------------------------*/
    if(NULL != pDataBuffer)
    {
        for(sReadCount=0; sReadCount<sReadSize; sReadCount++)
        {
            *pDest++ = *pSrc++;
        }
    }
    return sReadCount;
}

/*************************************************************************/
/** Function Name:  GB2312_GetFontData                                  **/
/** Purpose:        Read character data form font data.                 **/
/** Resources:      None.                                               **/
/** Params:                                                             **/
/** @ sStartAddr[in]: Read start address in memory.                     **/
/** @ pDataBuffer[in]: Character data dump buffer pointer.              **/
/** @ sReadSize[in]: Size of data will be read, always mean the buffer  **/
/**                 size.                                               **/
/** Return:         Data in process was be read.                        **/
/*************************************************************************/
static SGUI_BOOL IsFullWidth_Chinse(SGUI_UINT32 uiCode)
{
    /*----------------------------------*/
    /* Variable Declaration             */
    /*----------------------------------*/
    SGUI_BOOL                   bReturn;

    /*----------------------------------*/
    /* Process                          */
    /*----------------------------------*/
    if(uiCode < 0x7F)  /* ASCII字符 */
    {
        bReturn = SGUI_FALSE;
    }
    else    /* 汉字 */
    {
        bReturn = SGUI_TRUE;
    }

    return bReturn;
}
